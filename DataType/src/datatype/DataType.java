
package datatype;
public class DataType {
    public static void main(String[] args) {
        
        boolean b=true;
        int i=1023;
        char c='a';
        float f=10.25f;//here we have to use f after enter float number
        double d=11.25;
        short s=12343;
        System.out.printf("boolean b = %b\n",b);       //here we have to use + 
        System.out.println("int i ="+i);//here we have to use + 
        System.out.printf("char c =%s \n",c);//here we have to use + 
        System.out.printf("float f = %.2f\n",f);//here we have to use + 
        System.out.printf("double d = %.2f\n",d);//here we have to use + 
        System.out.printf("short s =%d \n",s);//here we have to use + 
       
    }
    
}
