    private void jButtonSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSubmitActionPerformed
        
        try {
            // TODO add your handling code here:
            Class.forName("com.mysql.cj.jdbc.Driver");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(BiodataClass.class.getName()).log(Level.SEVERE, null, ex);
        }
       
        String user="root";
        String password="";
        String url ="jdbc:mysql://127.0.0.1:3306/student?serverTimezone=UTC";
       
            Connection conn = null;
        try {
            conn = DriverManager.getConnection(url, user, password);
        } catch (SQLException ex) {
            Logger.getLogger(BiodataClass.class.getName()).log(Level.SEVERE, null, ex);
        }
       Statement statement =null;
        try {
               statement = conn.createStatement();
        } catch (SQLException ex) {
            Logger.getLogger(BiodataClass.class.getName()).log(Level.SEVERE, null, ex);
        }
        String query= "INSERT INTO `studentinfo`(`name`, `id`, `email`, `session`, `gender`, `password`, `conformpassword`)  VALUES (?,?,?,?,?,?,?)";
          
          
            try {
                PreparedStatement pst=conn.prepareStatement(query);
                 pst.setString(1,jTextFieldName.getText());
                 pst.setString(2,jTextFieldId.getText());
                 pst.setString(3,jTextFieldEmail.getText());
                 pst.setString(4,jTextFieldSession.getText());
                 pst.setString(5,jTextFieldGender.getText());
                 pst.setString(6,jPasswordFieldPassword.getText());
                 pst.setString(7,jPasswordFieldConformpassword.getText());
                 
                 
                 String pass1=jPasswordFieldPassword.getText();
                 String pass2=jPasswordFieldConformpassword.getText();
                 if(pass1.equals(pass2))
                 {
                     pst.executeUpdate();
                     String s=jTextFieldName.getText(); 
                     JOptionPane.showMessageDialog(this ,s+" Your Account Create Successful ! " );
                     LoginStudentClass a= new LoginStudentClass();
                     a.setVisible(true);
                     BiodataClass.this.dispose();
                 }
                 
                 else
                 {
                      String s=jTextFieldName.getText();
                      JOptionPane.showMessageDialog(this ,s+" Please cheack your password ! " );
                      jPasswordFieldConformpassword.setText("");
                 }
            } catch (SQLException ex) {
                Logger.getLogger(BiodataClass.class.getName()).log(Level.SEVERE, null, ex);
            }
        try {
            statement.executeUpdate(query);
        } catch (SQLException ex) {
            Logger.getLogger(BiodataClass.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            statement.close();
        } catch (SQLException ex) {
            Logger.getLogger(BiodataClass.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            conn.close();
        } catch (SQLException ex) {
            Logger.getLogger(BiodataClass.class.getName()).log(Level.SEVERE, null, ex);
        }
          
    }//GEN-LAST:event_jButtonSubmitActionPerformed

