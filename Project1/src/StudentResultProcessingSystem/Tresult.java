    private void jButtonSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSubmitActionPerformed
        // TODO add your handling code here:

        try {
            // TODO add your handling code here:
            Class.forName("com.mysql.cj.jdbc.Driver");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(BiodataClass.class.getName()).log(Level.SEVERE, null, ex);
        }

        Connection conn = null;
        try {
            conn = DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/student?serverTimezone=UTC", "root", "");
        } catch (SQLException ex) {
            Logger.getLogger(BiodataClass.class.getName()).log(Level.SEVERE, null, ex);
        }
        Statement statement =null;
        try {
            statement = conn.createStatement();
        } catch (SQLException ex) {
            Logger.getLogger(BiodataClass.class.getName()).log(Level.SEVERE, null, ex);
        }
        String query= "INSERT INTO studentresult(id, dld, java, math, accounting, electronics, stasitics, total) VALUES (?,?,?,?,?,?,?,?) ";

        try {
            PreparedStatement pst=conn.prepareStatement(query);
            pst.setString(1,jTextFieldId.getText());

            String java=jTextFieldJava.getText();
            String dld=jTextFieldDld.getText();
            String math=jTextFieldMath.getText();
            String acc=jTextFieldAccounting.getText();
            String ele=jTextFieldElectronics.getText();
            String sta=jTextFieldStasitics.getText();

            pst.setString(2,dld);
            pst.setString(3,java);
            pst.setString(4,math);
            pst.setString(5,acc);
            pst.setString(6,ele);
            pst.setString(7,sta);

            double total=18;
            double d=Double.parseDouble(dld);
            double j=Double.parseDouble(java);
            double m=Double.parseDouble(math);
            double a=Double.parseDouble(acc);
            double e=Double.parseDouble(ele);
            double s=Double.parseDouble(sta);

            double result;
            result=(((d*3)+(j*3)+(m*3)+(a*3)+(e*3)+(s*3))/total);

            String res=""+result;
            //  jTextFieldTotal.setText(res);
            pst.setString(8, res);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this ," Data Insert Successful and Totat CGPA is  " +res);
          

        } catch (SQLException ex) {
            //  Logger.getLogger(BiodataClass.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            statement.executeUpdate(query);
        } catch (SQLException ex) {
            // Logger.getLogger(BiodataClass.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            statement.close();
        } catch (SQLException ex) {
            // Logger.getLogger(BiodataClass.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            conn.close();
        } catch (SQLException ex) {
            //  Logger.getLogger(BiodataClass.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButtonSubmitActionPerformed

