    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        // TODO add your handling code here:
        String name= jTextFieldName.getText();
        String pass= jPasswordFieldPassword.getText();
       
       
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(LoginStudentClass.class.getName()).log(Level.SEVERE, null, ex);
        }
           Connection conn = null;
        try {
            conn = DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/student?serverTimezone=UTC", "root", "");
        } catch (SQLException ex) {
            Logger.getLogger(LoginStudentClass.class.getName()).log(Level.SEVERE, null, ex);
        }
            String query="SELECT `password` FROM `studentinfo` WHERE `name`='"+jTextFieldName.getText()+"'";
           PreparedStatement pst = null;
        try {
            pst = conn.prepareStatement(query);
        } catch (SQLException ex) {
            Logger.getLogger(LoginStudentClass.class.getName()).log(Level.SEVERE, null, ex);
        }
           ResultSet  result = null;
        try {
            result = pst.executeQuery();
        } catch (SQLException ex) {
            Logger.getLogger(LoginStudentClass.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            if(result.next())
            {
                String paw=result.getString("Password");
                if(paw.equals(pass))
                {
                    JOptionPane.showMessageDialog(null, "Welcome " +name);
                    StudentChooseClass a= new StudentChooseClass();
                    a.setVisible(true);
                    LoginStudentClass.this.dispose();
                    
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Your Password is wrong ");
                    
                }
            } 
        } catch (SQLException ex) {
            Logger.getLogger(LoginStudentClass.class.getName()).log(Level.SEVERE, null, ex);
        }
                      
       
    }//GEN-LAST:event_jButtonLoginActionPerformed

